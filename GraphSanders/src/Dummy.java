
public class Dummy 
{
	double time;
	int drinks;
	public Dummy(double time, int drinks) 
	{
		this.time = time;
		this.drinks = drinks;
	}
	public double getTime() 
	{
		return time;
	}
	public void setTime(double time) 
	{
		this.time = time;
	}
	public int getDrinks() 
	{
		return drinks;
	}
	public void setDrinks(int drinks)
	{
		this.drinks = drinks;
	}
	
}
